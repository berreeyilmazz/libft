#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*temp;

	if (!lst || !*lst || !del)
		return ;
	while (lst && *lst)
	{
		ft_lstdelone(*lst, del);
		temp = (*lst)->next;
		*lst = temp;
	}
}
